from __future__ import annotations

import io
import json
import wave
from unittest.mock import MagicMock

import numpy as np
import pytest

from justsayit.config import Config
from justsayit.transcribe_openai import (
    OpenAIWhisperTranscriber,
    _build_multipart,
    _encode_wav,
)


# ---------------------------------------------------------------------------
# WAV encoding
# ---------------------------------------------------------------------------


def test_encode_wav_round_trips_through_wave_module():
    """Generated bytes must be a parseable mono-16-bit WAV at the
    requested sample rate — that's what every Whisper-style server
    accepts."""
    rng = np.random.default_rng(0)
    samples = rng.uniform(-0.5, 0.5, size=8000).astype(np.float32)
    wav_bytes = _encode_wav(samples, 16000)
    with wave.open(io.BytesIO(wav_bytes), "rb") as r:
        assert r.getnchannels() == 1
        assert r.getsampwidth() == 2
        assert r.getframerate() == 16000
        assert r.getnframes() == 8000


def test_encode_wav_clips_extreme_values():
    """Float samples outside [-1, 1] must clip to int16 range, not wrap."""
    samples = np.array([1.5, -1.5, 0.0], dtype=np.float32)
    wav_bytes = _encode_wav(samples, 16000)
    with wave.open(io.BytesIO(wav_bytes), "rb") as r:
        pcm = np.frombuffer(r.readframes(3), dtype=np.int16)
    assert pcm[0] == 32767
    assert pcm[1] == -32768
    assert pcm[2] == 0


