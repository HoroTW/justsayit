def test_build_multipart_includes_required_fields():
    body, content_type = _build_multipart(
        wav_bytes=b"\x00\x01RIFFfake",
        model="whisper-1",
        language="en",
    )
    assert content_type.startswith("multipart/form-data; boundary=")
    boundary = content_type.split("boundary=", 1)[1]
    assert boundary.encode() in body
    # File part with the right disposition
    assert b'name="file"; filename="audio.wav"' in body
    assert b"Content-Type: audio/wav" in body
    assert b"\x00\x01RIFFfake" in body
    # Form fields
    assert b'name="model"' in body
    assert b"whisper-1" in body
    assert b'name="response_format"' in body
    assert b"json" in body
    assert b'name="language"' in body
    assert b"en" in body
    # Closing boundary
    assert body.endswith(f"--{boundary}--\r\n".encode())


def test_build_multipart_omits_language_when_blank():
    body, _ = _build_multipart(
        wav_bytes=b"x",
        model="whisper-1",
        language="",
    )
    assert b'name="language"' not in body


