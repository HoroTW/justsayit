def _make_cfg(**overrides):
    cfg = Config()
    cfg.model.backend = "openai"
    cfg.model.openai_endpoint = "https://api.example.com/v1"
    cfg.model.openai_model = "whisper-1"
    cfg.model.openai_api_key = "sk-test"
    for k, v in overrides.items():
        setattr(cfg.model, k, v)
    return cfg


def _fake_urlopen_returning(payload: bytes):
    def fake(req, timeout=None):
        resp = MagicMock()
        resp.read.return_value = payload
        resp.__enter__ = lambda self: self
        resp.__exit__ = lambda self, *a: False
        fake.captured = {
            "url": req.full_url,
            "headers": dict(req.header_items()),
            "data": req.data,
            "timeout": timeout,
        }
        return resp
    return fake


def test_transcribe_posts_to_audio_transcriptions(monkeypatch):
    cfg = _make_cfg()
    t = OpenAIWhisperTranscriber(cfg)
    fake = _fake_urlopen_returning(json.dumps({"text": "  hello world "}).encode())
    import justsayit.transcribe_openai as mod
    monkeypatch.setattr(mod.urllib.request, "urlopen", fake)

    samples = np.zeros(1600, dtype=np.float32)
    out = t.transcribe(samples, 16000)
    assert out == "hello world"
    assert fake.captured["url"] == "https://api.example.com/v1/audio/transcriptions"
    assert fake.captured["headers"]["Authorization"] == "Bearer sk-test"
    assert fake.captured["headers"]["Content-type"].startswith("multipart/form-data")
    # The body must contain a real WAV (RIFF header)
    assert b"RIFF" in fake.captured["data"]
    assert fake.captured["timeout"] == pytest.approx(60.0)


def test_transcribe_includes_language_when_set(monkeypatch):
    cfg = _make_cfg(openai_language="de")
    t = OpenAIWhisperTranscriber(cfg)
    fake = _fake_urlopen_returning(json.dumps({"text": "hallo"}).encode())
    import justsayit.transcribe_openai as mod
    monkeypatch.setattr(mod.urllib.request, "urlopen", fake)

    t.transcribe(np.zeros(1600, dtype=np.float32), 16000)
    assert b'name="language"' in fake.captured["data"]
    assert b"\r\n\r\nde\r\n" in fake.captured["data"]


def test_transcribe_handles_text_response_format(monkeypatch):
    """Some servers return text/plain; JSON parse fails → fall back to
    decoded body."""
    cfg = _make_cfg()
    t = OpenAIWhisperTranscriber(cfg)
    fake = _fake_urlopen_returning(b"hello plain text\n")
    import justsayit.transcribe_openai as mod
    monkeypatch.setattr(mod.urllib.request, "urlopen", fake)

    out = t.transcribe(np.zeros(1600, dtype=np.float32), 16000)
    assert out == "hello plain text"


def test_transcribe_empty_samples_returns_empty():
    """Don't waste an API call (or quota) on an empty buffer."""
    cfg = _make_cfg()
    t = OpenAIWhisperTranscriber(cfg)
    out = t.transcribe(np.array([], dtype=np.float32), 16000)
    assert out == ""


def test_transcribe_raises_when_no_key(monkeypatch, tmp_path):
    import justsayit.config as cfg_mod
    monkeypatch.setattr(cfg_mod, "config_dir", lambda: tmp_path)
    cfg_mod._DOTENV_LOADED = False
    monkeypatch.delenv("OPENAI_API_KEY", raising=False)

    cfg = _make_cfg(openai_api_key="")
    t = OpenAIWhisperTranscriber(cfg)
    with pytest.raises(RuntimeError, match="OPENAI_API_KEY"):
        t.transcribe(np.zeros(1600, dtype=np.float32), 16000)


